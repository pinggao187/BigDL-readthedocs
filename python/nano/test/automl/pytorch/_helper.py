import torch

from torch.utils.data import DataLoader, Dataset
from pytorch_lightning import LightningModule


class RandomDataset(Dataset):
    """
    Random Dataset.

    This class is modified from RandomDataset in
    https://github.com/PyTorchLightning/pytorch-lightning/
    blob/master/pytorch_lightning/demos/boring_classes.py

    :param _type_ Dataset: _description_
    """

    def __init__(self, size: int, length: int):
        self.len = length
        self.data = torch.randn(length, size)

    def __getitem__(self, index):
        return self.data[index]

    def __len__(self) -> int:
        return self.len


class BoringModel(LightningModule):
    """
    A Simple PL Model.

    This model is adapted from BoringModel in
    https://github.com/PyTorchLightning/pytorch-lightning/
    blob/master/pytorch_lightning/demos/boring_classes.py
    """
    def __init__(self):
        super().__init__()
        layers = [torch.nn.Linear(32, 2)]
        self.layers = torch.nn.Sequential(*layers)

    def forward(self, x):
        return self.layers(x)

    def loss(self, batch, prediction):
        # An arbitrary loss to have a loss that updates the model weights during `Trainer.fit` calls
        return torch.nn.functional.mse_loss(prediction, torch.ones_like(prediction))

    def mae(self, batch, prediction):
        return torch.nn.functional.l1_loss(prediction, torch.ones_like(prediction))

    def step(self, x):
        x = self(x)
        out = torch.nn.functional.mse_loss(x, torch.ones_like(x))
        return out

    def training_step(self, batch, batch_idx):
        output = self(batch)
        loss = self.loss(batch, output)
        return {"loss": loss}

    def training_step_end(self, training_step_outputs):
        return training_step_outputs

    def training_epoch_end(self, outputs) -> None:
        torch.stack([x["loss"] for x in outputs]).mean()

    def validation_step(self, batch, batch_idx):
        output = self(batch)
        loss = self.loss(batch, output)
        self.log("val_loss", loss)
        self.log("val_mae", self.mae(batch, output))
        # self.log("hp_metric", accuracy, on_step=False, on_epoch=True)
        return {"x": loss}

    def validation_epoch_end(self, outputs) -> None:
        torch.stack([x["x"] for x in outputs]).mean()

    def test_step(self, batch, batch_idx):
        output = self(batch)
        loss = self.loss(batch, output)
        return {"y": loss}

    def test_epoch_end(self, outputs) -> None:
        torch.stack([x["y"] for x in outputs]).mean()

    def configure_optimizers(self):
        optimizer = torch.optim.Adam(self.layers.parameters(),lr=0.1)
        lr_scheduler = torch.optim.lr_scheduler.StepLR(optimizer, step_size=1)
        return [optimizer], [lr_scheduler]

    def train_dataloader(self):
        return DataLoader(RandomDataset(32, 64))

    def val_dataloader(self):
        return DataLoader(RandomDataset(32, 64))

    def test_dataloader(self):
        return DataLoader(RandomDataset(32, 64))

    def predict_dataloader(self):
        return DataLoader(RandomDataset(32, 64))